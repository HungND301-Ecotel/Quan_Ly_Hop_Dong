export const getStatistics = () => {
  const activeTemplates = mockContractTemplates.filter(
    (c) => c.status === 'ACTIVE'
  ).length;
  const activeEconomic = mockEconomicContracts.filter(
    (c) => c.status === 'ACTIVE'
  ).length;
  const totalDebt = mockDebtRecords.reduce(
    (sum, d) => sum + d.outstandingAmount,
    0
  );
  const overdueDebt = mockDebtRecords
    .filter((d) => d.overdueDays > 0)
    .reduce((sum, d) => sum + d.outstandingAmount, 0);
  const pendingApprovals = mockApprovalLogs.filter(
    (l) => l.status === 'PENDING'
  ).length;

  return {
    activeTemplates,
    activeEconomic,
    totalDebt,
    overdueDebt,
    pendingApprovals,
  };
};
